package com.ibm.scas.analytics.backend;

import com.ibm.scas.analytics.utils.CPEException;

public class ProvisioningException extends CPEException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ProvisioningException() {
		super();
	}

	public ProvisioningException(String arg0) {
		super(arg0);
	}

	public ProvisioningException(Throwable arg0) {
		super(arg0);
	}

	public ProvisioningException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
